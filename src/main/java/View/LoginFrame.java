    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
     
        try{
            
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());
        
        Connection conn = DatabaseConnection.getConnection();
    
        PreparedStatement ps = conn.prepareStatement("Select * from Admins where Username = ? AND Password = ?");
        ps.setString(1, username);
        ps.setString(2, password);
        
        ResultSet rs = ps.executeQuery();
        
        if(rs.next()){
           HomePage homePage = new  HomePage();
           homePage.setVisible(true);
           LoginFrame.this.setVisible(false); 
        }
        else{
            if(username.equals("") || password.equals("")){
                JOptionPane.showMessageDialog(rootPane, "Please fill in all fields.");
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "√ènvalid username or password!");
            }
          }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

